import java.math.BigInteger;
import java.util.Scanner;
public class Expectation {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    int k = sc.nextInt();
    int n = sc.nextInt();
    solve(k, n);
    sc.close();
  }
  
  static void solve(int k, int n) {
    BigInteger bign = new BigInteger("" + (n - 2));
    BigInteger denom = new BigInteger("" + 2);
    BigInteger numer = new BigInteger("" + 2);
    BigInteger gcd = null;
    
    switch (k) {
      case 1 : {
        // (2/3)*n
        numer = bign.multiply(new BigInteger("" + 2));
        denom = new BigInteger("" + 3);
        break;
      }
      case 2 : {
        // (40*n^2+16n+3)/90
        numer = bign.pow(2).multiply(new BigInteger("" + 40))
            .add(bign.multiply(new BigInteger("" + 16)))
            .add(new BigInteger("" + 3));
        denom = new BigInteger("" + 90);
        break;
      }
      case 3 : {
        // (280n^3+336n^2+47n-48)/945
        numer = bign.pow(3).multiply(new BigInteger("" + 280))
            .add(bign.pow(2).multiply(new BigInteger("" + 336)))
            .add(bign.multiply(new BigInteger("" + 47)))
            .subtract(new BigInteger("" + 48));
        denom = new BigInteger("" + 945);
        break;
      }
      case 4 : {
        // (2800n^4+6720n^3+1964n^2-2472n+423)/14175
        numer = bign.pow(4).multiply(new BigInteger("" + 2800))
            .add(bign.pow(3).multiply(new BigInteger("" + 6720)))
            .add(bign.pow(2).multiply(new BigInteger("" + 1964)))
            .subtract(bign.multiply(new BigInteger("" + 2472)))
            .add(new BigInteger("" + 423));
        denom = new BigInteger("" + 14175);
        break;
      }
      case 5 : {
        // (49280n^5+ 197120n^4+2888n^3-3024n^2-1917n+4220)/8505
        numer = bign.pow(5).multiply(new BigInteger("" + 1120))
            .add(bign.pow(4).multiply(new BigInteger("" + 4480)))
            .add(bign.pow(3).multiply(new BigInteger("" + 2888)))
            .subtract(bign.pow(2).multiply(new BigInteger("" + 3024)))
            .subtract(bign.multiply(new BigInteger("" + 1917)))
            .add(new BigInteger("" + 4220));
        denom = new BigInteger("" + 8505);
        break;
      }
    }
    gcd = numer.gcd(denom);
    numer = numer.divide(gcd);
    denom = denom.divide(gcd);
    System.out.println(numer + " / " + denom);
  }
  
}

